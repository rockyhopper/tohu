package org.tohu.support;


/**
 * <p>
 * Represents a derivation of data entered by a user.
 * </p>
 * 
 * <p>
 * <code>Variable</code> has an <code>answerType</code> which must be one of:
 * </p>
 * 
 * <ul>
 * <li><code>text</code></li>
 * <li><code>number</code></li>
 * <li><code>decimal</code></li>
 * <li><code>boolean</code></li>
 * <li><code>date</code></li>
 * </ul>
 * 
 * <p>
 * or an extension of one of these using the notation <code>&lt;type&gt;.&lt;extension type&gt; </code> e.g.
 * <code>text.url</code> or <code>decimal.currency</code>.
 * </p>
 * 
 * <p>
 * The answer to a <code>Variable</code> is maintained internally by the object. use <code>DomainModelAssociation</code> to
 * map the answers to a real domain model.
 * </p>
 * 
 * @author Derek Rendall
 */
public class Variable extends TohuDataItemObject {

	private static final long serialVersionUID = 1L;

	private String name;
	
	public Variable() {
		super();
	}

	public Variable(String id) {
		super(id);
	}

	public Variable(String id, String name) {
		super(id);
		this.name = name;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}	
	
	/**
	 * For debugging purposes.
	 */
	@Override
	public String toString() {
		return super.toString() + " name=" + getName();
	}



}
